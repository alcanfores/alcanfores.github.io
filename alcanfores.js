function toggleLanguage() {
    const checkbox = document.getElementById('language-toggle');
    const newLang = checkbox.checked ? 'en' : 'es';
    
    // Actualiza el idioma actual
    currentLang = newLang;
    
    // Traduce la página
    translatePage();
    
    // Cambia la imagen del WiFi
    toggleWifiImage();
}

function translatePage() {
    const elements = document.querySelectorAll('[data-translate]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[key]) {
            element.innerHTML = translations[key][currentLang];
        }
    });

    document.documentElement.lang = currentLang;
}

function toggleWifiImage() {
    const wifiDataEs = document.getElementById('wifiDataEs');
    const wifiDataEn = document.getElementById('wifiDataEn');
    
    if (currentLang === 'en') {
        wifiDataEs.style.display = 'none';
        wifiDataEn.style.display = 'block';
    } else {
        wifiDataEs.style.display = 'block';
        wifiDataEn.style.display = 'none';
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const languageToggle = document.getElementById('language-toggle');
    languageToggle.addEventListener('change', toggleLanguage);
});