let currentLang = 'es';

function translate() {
    const newLang = currentLang === 'es' ? 'en' : 'es';
    const elements = document.querySelectorAll('[data-translate]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[key]) {
            element.innerHTML = translations[key][newLang];
        }
    });

    // Cambiar la imagen del WiFi
    const wifiDataEs = document.getElementById('wifiDataEs');
    const wifiDataEn = document.getElementById('wifiDataEn');
    
    if (newLang === 'en') {
        wifiDataEs.style.display = 'none';
        wifiDataEn.style.display = 'block';
    } else {
        wifiDataEs.style.display = 'block';
        wifiDataEn.style.display = 'none';
    }

    currentLang = newLang;
    document.documentElement.lang = newLang;
    
    // Actualizar el texto del botón
    const translateBtn = document.getElementById('translateBtn');
    translateBtn.textContent = translations['translateBtn'][newLang];
}

